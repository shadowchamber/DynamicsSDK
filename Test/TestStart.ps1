<#
.SYNOPSIS
    Prepare the environment for test execution.

.DESCRIPTION
    This script is intended for use in the Dynamics AX Development environment
    to automate the test execution process. It will start the Dynamics AX 7.0
    deployment, configures the user, and log the test run start event.

.NOTES
    Copyright Â© 2016 Microsoft. All rights reserved.
#>
[Cmdletbinding()]
Param(
    [Parameter(Mandatory=$true, HelpMessage="The build number for this test run.")]
    [string]$BuildNumber
)

# Import module for Write-Message and other common functions (Picks up $LogPath variable).
Import-Module $(Join-Path -Path $PSScriptRoot -ChildPath "..\DynamicsSDKCommon.psm1") -Function "Write-Message", "Get-AX7SdkPath", "Start-AX7Deployment"

Function SetPipelineVariable
{
    Param([string]$VSInstallationPath)
    # Get the Visual Studio path and set the Vsix Extension path.
    $vsextensionsfolder = $(Join-Path -Path $VSInstallationPath -ChildPath "\Common7\IDE\Extensions\")
    [System.Version]$maxVersion = $null
    $vsixExtensionFullPath = $null

    foreach ($item in Get-ChildItem -Path $vsextensionsfolder -Filter "AOSKernel.dll" -Recurse -ErrorAction SilentlyContinue)
    {
        [string]$AOSKernelJsonFile = Join-Path -Path ($item.DirectoryName) -ChildPath "AOSKernel.dll"

        if ( (Test-Path -Path $AOSKernelJsonFile) -eq $true)
        {
            [System.Version]$CurrentAOSKernelVersion = [System.Diagnostics.FileVersionInfo]::GetVersionInfo("$AOSKernelJsonFile").FileVersion
            Write-Message "CurrentVersion - $CurrentAOSKernelVersion"
            if ( ($maxVersion -eq $null) -or ($CurrentAOSKernelVersion -gt $maxVersion) ) 
            {
                $maxVersion = $CurrentAOSKernelVersion
                $vsixExtensionFullPath = ($item.DirectoryName)
                Write-Message "MaxVersion is $maxVersion"
                Write-Message "VisualStudio ExtensionPath is $vsixExtensionFullPath"
            }
        }
    }

    Write-Host "##vso[task.setvariable variable=VsixExtensionFolder]$vsixExtensionFullPath"
}

[int]$ExitCode = 0
try
{
    # Check VisualStudio version
    $vswherePath = $("${env:ProgramFiles(x86)}\Microsoft Visual Studio\Installer\vswhere.exe")
    if (Test-Path $vswherePath)
    {
        [xml]$vsInstances = & $vswherePath -format xml -version "[15.0, 16.0)" 
        if ($vsInstances.instances -eq $null)
        {
            Write-Message "Microsoft Visual Studio 2017 is not found" -Warning
            $vsInstallationPath = & $vswherePath -legacy -version '[14.0,15.0)' -property installationPath
            Write-Message "Microsoft Visual Studio 2015 is found at path $($vsInstallationPath)"
            SetPipelineVariable -VSInstallationPath $vsInstallationPath
        }
        else
        {
            if ($vsInstances.instances.instance.Count -gt 0)
            {
                Write-Message "Found $($vsInstances.instances.instance.Count) Visual Studio instances"
                $VSInstallationPath = $vsInstances.instances.instance[0].installationPath
                Write-Message "First VisualStudio instance - $VSInstallationPath"
            }
            else
            {
                $vsInstallationPath = $vsInstances.instances.instance.installationPath
                Write-Message "Microsoft Visual Studio is found at path $($vsInstallationPath)"
            }

            SetPipelineVariable -VSInstallationPath $vsInstallationPath
        }
    }

    # Ensure that the AX deployment is started (stopped by build process).
    Write-Message "Starting Dynamics AX deployment..."
    Start-AX7Deployment
    
    $DynamicsSdkPath = Get-AX7SdkPath
    if ($DynamicsSdkPath -and (Test-Path -Path $DynamicsSdkPath))
    {
        $InstrumentationScript = Join-Path -Path $DynamicsSdkPath -ChildPath "DevALMInstrumentor.ps1"
        & $InstrumentationScript -TestStart -BuildNumber $BuildNumber
    }
    else
    {
        Write-Message "No test start even logged as the Dynamics SDK path could not be found at: $DynamicsSdkPath"
    }

    Write-Message "Preparing environment to start test execution complete."
}
catch [System.Exception]
{
    Write-Message "Error preparing environment for test execution: $($_)" -Error
    $ExitCode = -1
}
Write-Message "Script completed with exit code: $ExitCode"
Exit $ExitCode

